package org.arslan.vulnarity.model;

public class xssModel {
	
	private String xssVulnerability;
	private String xssUsage;
	private String xssParameter;
	
	public String getXssVulnerability() {
		return xssVulnerability;
	}
	public void setXssVulnerability(String xssVulnerability) {
		this.xssVulnerability = xssVulnerability;
	}
	public String getXssUsage() {
		return xssUsage;
	}
	public void setXssUsage(String xssUsage) {
		this.xssUsage = xssUsage;
	}
	public String getXssParameter() {
		return xssParameter;
	}
	public void setXssParameter(String xssParameter) {
		this.xssParameter = xssParameter;
	}
}
